    private void btnSuaDiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaDiemActionPerformed
        // TODO add your handling code here:
        String ID = lbID.getText();
        String TV = txtTV.getText();
        String Toan = txtT.getText();
        String LSDL = txtLSDL.getText();
        String AmNhac = txtAN.getText();
        String MyThuat = txtMT.getText();
        String TheDuc = txtTD.getText();
        String DaoDuc = txtDD.getText();
        
        String rgdiem = "^(?:[0-9]|0[0-9]|10)$"; 
        if(TV.equals("") || Toan.equals("") || LSDL.equals("") || AmNhac.equals("") || MyThuat.equals("") || TheDuc.equals("") || DaoDuc.equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đủ thông tin");
        }
        else {
            if(!TV.matches(rgdiem) || !Toan.matches(rgdiem) || !LSDL.matches(rgdiem) || !AmNhac.matches(rgdiem) || !MyThuat.matches(rgdiem) || !TheDuc.matches(rgdiem) || !DaoDuc.matches(rgdiem)) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập điểm (0-10)");
            }
            else {
                DiemDTO diem = new DiemDTO(ID, TV, Toan, LSDL, AmNhac, MyThuat, TheDuc, DaoDuc);
                DiemBLL.update(diem);
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                showDiem(0,10);
            }
        } 
    }//GEN-LAST:event_btnSuaDiemActionPerformed

