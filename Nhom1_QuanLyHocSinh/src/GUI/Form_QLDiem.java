    private void showDiem(float min, float max) {
        diemList =  DiemBLL.getAll();
        dtm.setRowCount(0);
        
        diemList.forEach((DiemDTO) -> {
            if(DiemDTO.getDTB() >= min && DiemDTO.getDTB() < max){
                dtm.addRow(new Object[] {DiemDTO.getID(), DiemDTO.getHoTen(), DiemDTO.getLop(), 
                DiemDTO.getTiengViet(), DiemDTO.getToan(),DiemDTO.getLSDL(),
                DiemDTO.getAmNhac(),DiemDTO.getMyThuat(),DiemDTO.getTheDuc(),
                DiemDTO.getDaoDuc(), DiemDTO.getDTB(), DiemDTO.getHocLuc()});
            };
        });
    }
    

